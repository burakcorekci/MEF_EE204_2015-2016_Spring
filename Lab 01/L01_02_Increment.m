% I will use some clearing commands at the start of every 
% script to clean Workspace and Command Window.
clear all; close all; clc;

% From 1 to 5, increment with 1
a = 1:1:5;
disp(a);

% From 1 to 5, increment with 0.5
b = 0:0.5:2;
disp(b);

% If the increment is 1, you can omit the increment part.
a = 1:5;
disp(a);

% You can also use this operation with negative step sizes.
c = 12:-2:0;
disp(c);








